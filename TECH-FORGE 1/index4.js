//Object: 
// O objeto base de todos os objetos em JavaScript. 
// Ele permite a criação de novos objetos e fornece métodos para 
//manipulação de propriedades, como 
// Object.keys(), Object.values() e Object.entries().

//Array: Um dos objetos mais utilizados, 
//permite armazenar e manipular coleções ordenadas de elementos. 
//Possui métodos úteis como, 
// push() para adicionar elementos, 
// pop() para remover o último item, 
// map() para transformar elementos e 
// filter() para filtragem de dados.

//String: Fornece propriedades e métodos para trabalhar com texto.
//  Métodos como 
// toUpperCase(), 
// toLowerCase(), 
// slice(), 
// split() e 
// replace() 
// são amplamente usados para manipulação de strings.

//Number: Representa valores numéricos e inclui métodos úteis como 
// toFixed() para formatar casas decimais e 
// parseFloat() para converter strings em números decimais.

//Boolean: Representa valores lógicos true ou false, 
// sendo essencial para operações condicionais 
// e controle de fluxo em JavaScript.

//Function: Todo bloco de código reutilizável em 
// JavaScript é tratado como um objeto Function.
//  Ele permite a criação de funções personalizadas, 
// podendo ser atribuídas a variáveis e passadas como argumentos.

//Date: Utilizado para manipulação de datas e horários.
// O objeto Date permite operações como 
// obtenção do ano,
// mês, dia e hora atual, 
// além de cálculos temporais.

//RegExp: Permite a criação de expressões regulares para busca e manipulação de strings. 
// Expressões regulares são essenciais para validações e formatações de texto.

//Math: Um objeto que fornece métodos para operações matemáticas avançadas, 
// como Math.sqrt() para raízes quadradas, 
// Math.pow() para potenciação e Math.random() para geração de números aleatórios.

//JSON (JavaScript Object Notation): Um formato leve para intercâmbio de dados, 
// amplamente utilizado em aplicações web. 
// O objeto JSON fornece métodos como JSON.stringify() para converter 
// objetos em strings e 
// JSON.parse() para converter strings JSON em objetos JavaScript.
